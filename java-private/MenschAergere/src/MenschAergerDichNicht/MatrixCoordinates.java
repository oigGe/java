package MenschAergerDichNicht;

public class MatrixCoordinates {
    private int xPos;
    private int yPos;

    public MatrixCoordinates(int xPos, int yPos) {
        this.xPos = xPos;
        this.yPos = yPos;
    }

    public int getxPos() {
        return xPos;
    }

    public int getyPos() {
        return yPos;
    }
}
